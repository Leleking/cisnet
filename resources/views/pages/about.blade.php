CCNA Security Certification is associate level Certification in Network Security Technologies offered by Cisco which covers basics of Network Security, Network Attacks, VPN's, Firewalls, Cisco Router-Switch Security and validates associate-level knowledge and skills required to secure Cisco networks. With a CCNA Security certification, a network professional demonstrates the skills required to develop a security infrastructure, recognize threats and vulnerabilities to networks, and mitigate security threats. The CCNA Security curriculum emphasizes core security technologies, the installation, troubleshooting and monitoring of network devices to maintain integrity, confidentiality and availability of data and devices, and competency in the technologies that Cisco uses in its security structure.
